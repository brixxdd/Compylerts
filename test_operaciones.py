# Probando operaciones con tipos diferentes
num = 42
texto = "hola"
booleano = True

# Intentando sumar un string y un entero
res1 = texto + num

# Intentando multiplicar un string y un entero
res2 = texto * num

# Intentando restar un booleano y un string 
res3 = booleano - texto

print(res1, res2, res3)
